          <h1 class="title">
            Findings
          </h1>
          <p class="c3"><span class="c4 subtitle">We built the first Scorecard to evaluate policing in California. Here&rsquo;s what we found.</span></p>
          <p class="c3 c9"><span class="c1"></span></p>
          <p class="c3"><span>Nationwide protests demanding an end to police violence have </span><span class="c10"><a class="c12" href="https://www.google.com/url?q=https://bit.ly/2vCY7j1&amp;sa=D&amp;ust=1557708223509000">shifted public opinion</a></span><span>&nbsp;over the </span><span>past five years</span><sup><a href="#cmnt1" id="cmnt_ref1">[a]</a></sup><span>. An estimated </span><span class="c10"><a class="c12" href="https://www.google.com/url?q=https://www.vox.com/2019/3/22/18259865/great-awokening-white-liberals-race-polling-trump-2020&amp;sa=D&amp;ust=1557708223509000">45 million</a></span><span>&nbsp;Americans have adopted more progressive views on race and racism since the protests </span><span>began</span><sup><a href="#cmnt2" id="cmnt_ref2">[b]</a></sup><span>&nbsp;in 2014. But while public opinion has changed, </span><span>policing</span><sup><a href="#cmnt3" id="cmnt_ref3">[c]</a></sup><span> outcomes in most places have not. The police killed </span><span class="c10"><a class="c12" href="https://www.google.com/url?q=https://www.theroot.com/here-s-how-many-people-police-killed-in-2018-1831469528&amp;sa=D&amp;ust=1557708223510000">more people</a></span><span>&nbsp;</span><span>last year than the year before</span><sup><a href="#cmnt4" id="cmnt_ref4">[d]</a></sup><span>, </span><span>racial disparities in outcomes such as </span><span class="c10"><a class="c12" href="https://www.google.com/url?q=https://ucr.fbi.gov/crime-in-the-u.s/2017/crime-in-the-u.s.-2017/tables/table-43&amp;sa=D&amp;ust=1557708223510000">arrests</a></span><span>&nbsp;and </span><span class="c10"><a class="c12" href="https://www.google.com/url?q=https://mappingpoliceviolence.org/&amp;sa=D&amp;ust=1557708223510000">deadly force</a></span><span>&nbsp;persist</span><sup><a href="#cmnt5" id="cmnt_ref5">[e]</a></sup><span>, and the criminal justice system is not more likely to hold police accountable. In a country with 18,000 law enforcement agencies, each with different issues and outcomes, changing these outcomes on a nationwide scale requires sustained organizing and advocacy efforts in </span><span class="c15">every jurisdiction</span><span>. To do this, </span><span class="c11 c4">communities need the tools to effectively evaluate each law enforcement agency and hold them accountable to measurable results. </span></p>
          <p class="c3 c9"><span class="c1"></span></p>
          <p class="c3"><span>But how do you evaluate the police?</span><span class="c4">&nbsp;</span><span>There are substantially different perspectives about what police should or should not do - and whether the institution of policing should continue to exist at all. These differences cannot all be resolved at once, but we believe there are a set of common principles that can provide an initial framework for evaluating </span><span>any </span><span class="c1">agency responsibility for protecting people from harm:</span></p>
          <p class="c3 c9"><span class="c1"></span></p>
          <ul class="c7 lst-kix_pr7wbpbcyk4v-0 start">
            <li class="c3 c8 c6"><span class="c1">The agency should prioritize protecting people from violence, not arresting people for low level offenses</span></li>
            <li class="c3 c8 c6"><span>The</span><span class="c1">&nbsp;agency should avoid the use of force, especially deadly force, to the greatest extent possible</span></li>
            <li class="c3 c8 c6"><span>When people come forward to report misconduct by employees of the agency, it </span><span>should result in some form of accountability</span><sup><a href="#cmnt6" id="cmnt_ref6">[f]</a></sup><span class="c1">&nbsp;</span></li>
            <li class="c3 c8 c6"><span>When people call on the agency to help solve the </span><span>most serious crimes - </span><sup><a href="#cmnt7" id="cmnt_ref7">[g]</a></sup><span class="c1">those resulting in death - they should be able to trust that agency to find the person responsible </span></li>
            <li class="c3 c8 c6"><span class="c1">The agency should accomplish these goals in ways that are not biased or discriminatory</span></li>
          </ul>
          <p class="c3 c9"><span class="c1"></span></p>
          <p class="c3"><span>Based on these principles, </span><span class="c10"><a class="c12" href="https://www.google.com/url?q=http://joincampaignzero.org&amp;sa=D&amp;ust=1557708223512000">Campaign Zero</a></span><span>&nbsp;obtained data from state and local agencies to evaluate California&rsquo;s 100 largest municipal police departments and converted each evaluation (represented by a &ldquo;score&rdquo; from 0-100) into an easy-to-understand letter grade. Using this methodology, a police department received a higher grade if it made </span><span class="c15">fewer arrests</span><span>&nbsp;for low level offenses, </span><span class="c15">used</span><span>&nbsp;</span><span class="c15">less force </span><span>during arrest, had </span><span class="c15">fewer homicides unsolved</span><span>, </span><span class="c15">did not have racial disparities</span><span>&nbsp;in arrests and use of force, and </span><span class="c15">upheld civilian complaints</span><span>&nbsp;of police misconduct more often than other police departments in the state. </span><span class="c4">See the grade each police department received, and the outcomes informing each grade, at </span><span class="c10 c4"><a class="c12" href="https://www.google.com/url?q=http://policescorecard.org&amp;sa=D&amp;ust=1557708223512000">policescorecard.org. </a></span></p>
          <p class="c3 c9"><span class="c1"></span></p>
          <p class="c3"><span class="c11 c4">Here are some of the major findings of our analysis:</span></p>
          <ol class="c7 lst-kix_fckzbmam001m-0 start" start="1">
            <li class="c3 c8 c6"><span>Most people arrested in California are arrested for low level offenses.</span><span class="c4">&nbsp;</span><span>Of 1,354,769 reported arrests made in 2016, 70% </span><span>were for misdemeanor offenses. Police made </span><span class="c4">1.8x </span><span>as many</span><span class="c4">&nbsp;</span><span>arrests for </span><span class="c4">drug possession</span><span>&nbsp;alone as they did for </span><span class="c4">all violent crimes combined.</span><sup class="c4"><a href="#ftnt1" id="ftnt_ref1">[1]</a></sup><span class="c11 c4">&nbsp;</span></li>
          </ol>
          <p class="c3 c6"><span style="overflow: hidden; display: inline-block; margin: 0.00px 0.00px; border: 0.00px solid #000000; transform: rotate(0.00rad) translateZ(0px); -webkit-transform: rotate(0.00rad) translateZ(0px); width: 373.50px; height: 172.98px;"><a href="assets/img/findings/image5.png" target="_"><img alt="" src="assets/img/findings/image5.png" style="width: 373.50px; height: 172.98px; margin-left: 0.00px; margin-top: 0.00px; transform: rotate(0.00rad) translateZ(0px); -webkit-transform: rotate(0.00rad) translateZ(0px);" title=""></a></span><sup><a href="#cmnt8" id="cmnt_ref8">[h]</a></sup><sup><a href="#cmnt9" id="cmnt_ref9">[i]</a></sup></p>
          <ol class="c7 lst-kix_fckzbmam001m-0" start="2">
            <li class="c3 c8 c6"><span>Police discharged their firearms or otherwise used force causing death or </span><span>serious bodily injury</span><sup><a href="#cmnt10" id="cmnt_ref10">[j]</a></sup><sup><a href="#ftnt2" id="ftnt_ref2">[2]</a></sup><span>&nbsp;in </span><span class="c4">1,276 incidents</span><span>&nbsp;from 2016-2017, killing 328 people and seriously injuring an additional 824 people. 647 of these incidents were police shootings, while the other half were other forms of police use of force that caused death or serious injury. </span><span class="c4">Overall</span><span>, </span><span class="c11 c4">half of people killed or seriously injured by police (49%) were unarmed. </span></li>
          </ol>
          <p class="c3 c9 c6"><span class="c1"></span></p>
          <p class="c3 c6"><span>Police in San Bernardino, Riverside, Stockton, Long Beach, Fremont and Bakersfield used deadly force at </span><span class="c4">substantially higher rates </span><span>than other major cities in California. San Jose and Los Angeles police used deadly force at 3x the rate of police in </span><span>San Francisco and San Diego</span><sup><a href="#cmnt11" id="cmnt_ref11">[k]</a></sup><sup><a href="#cmnt12" id="cmnt_ref12">[l]</a></sup><span>. </span><span>And Oakland police had one of the lowest rates of deadly force, reflecting the substantial decline in use of force incidents that has followed </span><span class="c10"><a class="c12" href="https://www.google.com/url?q=http://www.oaklandmagazine.com/April-2017/The-Year-of-No-Shootings/&amp;sa=D&amp;ust=1557708223515000">DOJ mandated reforms </a></span><span>to their use of force policies. </span><sup><a href="#cmnt13" id="cmnt_ref13">[m]</a></sup></p>
