          <p class="c3 c6"><span style="overflow: hidden; display: inline-block; margin: 0.00px 0.00px; border: 0.00px solid #000000; transform: rotate(0.00rad) translateZ(0px); -webkit-transform: rotate(0.00rad) translateZ(0px); width: 624.00px; height: 176.00px;"><a href="assets/img/findings/image3.png" target="_"><img alt="" src="assets/img/findings/image3.png" style="width: 624.00px; height: 176.00px; margin-left: 0.00px; margin-top: 0.00px; transform: rotate(0.00rad) translateZ(0px); -webkit-transform: rotate(0.00rad) translateZ(0px);" title=""></a></span></p>
          <p class="c3 c9"><span class="c1"></span></p>
          <p class="c3"><span>When these outcomes are evaluated together, it reveals a disturbing picture of policing within the state. </span><span class="c4">Most departments received a score lower than 60% - the equivalent of an F grade. </span><span>In some cases, these evaluations confirmed what has previously been reported. For example, Bakersfield Police Department, which has been </span><span class="c10"><a class="c12" href="https://www.google.com/url?q=https://www.theatlantic.com/politics/archive/2015/12/the-deadliest-county-for-police-killings-in-america/418359/&amp;sa=D&amp;ust=1557708223520000">cited</a></span><span>&nbsp;as one of the deadliest departments in the nation, received the 4th lowest score among the 100 California departments. Other departments received scores that were more unexpected. For example, Carlsbad Police Department received the highest score. Further exploration of the organizational culture, leadership and practices of this department might produce valuable insights into how to improve outcomes in</span><span>&nbsp;other police departments.</span><sup><a href="#cmnt16" id="cmnt_ref16">[p]</a></sup><span>&nbsp;By contrast, Beverly Hills Police Department received </span><span class="c10"><a class="c12" href="https://www.google.com/url?q=https://policescorecard.org/?city%3Dbeverly-hills&amp;sa=D&amp;ust=1557708223521000">the lowest score</a></span><span class="c1">&nbsp;of all 100 departments, due to relatively high levels of police violence, severe racial inequities in law enforcement and a system that almost never holds officers accountable for misconduct. </span></p>
          <p class="c3 c9"><span class="c1"></span></p>
          <p class="c3"><span>These findings should prompt further investigations and interventions targeting low-performing police departments within the state, not only from local policymakers but also potentially from the California Attorney General, who has the power to initiate pattern and practice investigations into local police agencies.</span></p>
